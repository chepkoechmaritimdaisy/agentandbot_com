defmodule GovernanceCore.AXAudit do
  @moduledoc """
  Runs a nightly audit of the application to ensure it remains "Agent-Friendly".
  Checks for:
  - Semantic HTML structure (presence of <main>, <h1>, <article>)
  - Accessibility of SKILL.md files
  - Low complexity (avoiding heavy JS blocking)
  """
  use GenServer
  require Logger

  # Continuous interval (5 minutes)
  @interval 5 * 60 * 1000

  def start_link(_opts) do
    GenServer.start_link(__MODULE__, %{}, name: __MODULE__)
  end

  def init(state) do
    schedule_audit()
    {:ok, state}
  end

  def handle_info(:audit, state) do
    perform_audit()
    schedule_audit()
    {:noreply, state}
  end

  defp schedule_audit do
    Process.send_after(self(), :audit, @interval)
  end

  def perform_audit do
    Logger.info("Starting Continuous AX Audit...")

    base_url = GovernanceCoreWeb.Endpoint.url()
    # Adding MCP endpoint to the check
    endpoints = ["/", "/agents", "/dashboard/traffic", "/api/mcp"]

    results = Enum.map(endpoints, fn path ->
      url = base_url <> path
      check_endpoint(url)
    end)

    failures = Enum.filter(results, fn {status, _} -> status == :error end)

    if Enum.empty?(failures) do
      Logger.info("AX Audit Passed: All endpoints are Agent-Friendly.")
    else
      Logger.error("AX Audit Failed: #{inspect(failures)}")
      create_automated_pr(failures)
    end
  end

  defp check_endpoint(url) do
    {time_us, result} = :timer.tc(fn -> Req.get(url, decode_body: false) end)
    # Convert microseconds to milliseconds
    time_ms = time_us / 1000

    if time_ms > 1000 do
      {:error, :timeout}
    else
      case result do
        {:ok, %{status: 200, body: body}} ->
          if String.ends_with?(url, "/api/mcp") do
            check_mcp_endpoint(url, body)
          else
            if is_agent_friendly?(body) do
              {:ok, url}
            else
              {:error, :not_agent_friendly}
            end
          end
        {:ok, %{status: _status}} ->
          {:error, :bad_status}
        {:error, _reason} ->
          {:error, :timeout}
      end
    end
  end

  defp check_mcp_endpoint(url, body) do
    try do
      Jason.decode!(body)
      {:ok, url}
    rescue
      _e -> {:error, :invalid_json}
    end
  end

  defp is_agent_friendly?(html) do
    has_main = String.contains?(html, "<main")
    has_h1 = String.contains?(html, "<h1")
    has_main && has_h1
  end

  defp create_automated_pr(failures) do
    Logger.info("Attempting to create automated PR for AX Audit failures...")

    try do
      # Deduplicate PRs
      case System.cmd("gh", ["pr", "list", "--search", "🤖 [AX Audit] Automated Fix in:title"]) do
        {pr_list_output, 0} ->
          if String.trim(pr_list_output) == "" do
            # No existing PR, proceed
            fix_file_path = Path.join(File.cwd!(), "priv/ax_audit_fix.txt")
            File.write!(fix_file_path, "Automated fix required for AX Audit failures: #{inspect(failures)}\nTimestamp: #{DateTime.utc_now()}")

            System.cmd("git", ["add", fix_file_path])
            System.cmd("git", ["commit", "-m", "🤖 [AX Audit] Automated Fix\n\nFixing agent-friendly regressions detected by AX Audit."])
            System.cmd("gh", ["pr", "create", "--title", "🤖 [AX Audit] Automated Fix", "--body", "Automated PR generated by AX Audit GenServer."])

            Logger.info("Automated PR created successfully.")
          else
            Logger.info("Automated PR already exists, skipping.")
          end
        {err, code} ->
          Logger.warning("gh pr list failed with code #{code}: #{err}")
      end
    rescue
      e in ErlangError -> Logger.warning("Failed to execute gh command (might be missing): #{inspect(e)}")
    end
  end
end
